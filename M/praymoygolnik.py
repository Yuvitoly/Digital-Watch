print('Стороны прямоугольника. Узнаем площадь и периметр')
a = float(input('Одна сторона: '))
b = float(input('Другая сторона: '))
print('Площадь равна: ', a * b, 'кв. см')
print('Периметр равен: ', 2 * (a + b), 'см')


